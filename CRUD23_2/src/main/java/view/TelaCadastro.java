    public void preencherTabela(){
        DefaultTableModel dm =  (DefaultTableModel) tblTarefa.getModel();
        tblTarefa.setModel(dm);
        dm.setRowCount(0);
        
        TarefaDAO tdao = new TarefaDAO();
        
        for(Tarefa tarefa: tdao.findAll()){  
            dm.addRow(
                new Object[]{
                 tarefa.getId(),
                 tarefa.getDescricao(),
                 tarefa.getLocal(),
                }
            );
        }
    }

