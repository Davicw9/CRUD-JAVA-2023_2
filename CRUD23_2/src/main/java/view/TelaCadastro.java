    private void btnRemoverActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoverActionPerformed
            
        

        DefaultTableModel dm = (DefaultTableModel) tblTarefa.getModel();

        int linhaSelecionada = tblTarefa.getSelectedRow();
        int id = (int) tblTarefa.getValueAt(linhaSelecionada, 0);

        TarefaDAO tdao = new TarefaDAO();
        tdao.excluirTarefa(id);
        
        dm.removeRow(tblTarefa.getSelectedRow());     
    }//GEN-LAST:event_btnRemoverActionPerformed

    private void btnAtualizarCadastroActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAtualizarCadastroActionPerformed
        
        int linhaSelecionada = tblTarefa.getSelectedRow();
        int id = (int) tblTarefa.getValueAt(linhaSelecionada, 0);
        
        TarefaDAO tdao = new TarefaDAO();
        tdao.findById(id);
        
        TelaAtualizar tela = new TelaAtualizar(id);
        
        
        tela.setVisible(true);
        //dispose();
        
    }//GEN-LAST:event_btnAtualizarCadastroActionPerformed

    private void btnPesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPesquisarActionPerformed
        String pesquisar = txtPesquisar.getText();
        TarefaDAO tdao = new TarefaDAO();
        tdao.findByLocal(pesquisar);
        
        DefaultTableModel dm =  (DefaultTableModel) tblTarefa.getModel();
        tblTarefa.setModel(dm);
        dm.setRowCount(0);
        
        
        
        for(Tarefa tarefa: tdao.findByLocal(pesquisar)){  
            dm.addRow(
                new Object[]{
                 tarefa.getId(),
                 tarefa.getDescricao(),
                 tarefa.getLocal(),
                }
            );
        }
        
        
    }//GEN-LAST:event_btnPesquisarActionPerformed

